int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int		i;

	i = 0;
	if (n == 0)
	{
		return (0);
	}
	while ((i < n - 1) && s1[0] != '\0' && s2[0] != '\0' && s1[i] == s2[i])
	{
		i++;
	}
	return (s1[i] - s2[i]);
}
